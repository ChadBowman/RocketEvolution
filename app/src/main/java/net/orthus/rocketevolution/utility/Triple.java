public class Triple<X, Y, Z> {

    //===== INSTANCE VARIABLES
    public X first;
    public Y second;
    public Z third;

    //===== CONSTRUCTOR
    public Triple(X first, Y second, Z third){
        this.first = first;
        this.second = second;
        this.third = third;
    }

} // Tripple
